#include "fillit.h"

int		ft_check_nb(char *str, int i, int neighbor)
{
	if (((i % 21) + 1) > 5 && str[i - 5] == '#')
		neighbor++;
	if (((i % 21) + 1) < 15 && str[i + 5] == '#')
		neighbor++;
	if ((((i % 21) + 1) % 5) < 4 && str[i + 1] == '#')
		neighbor++;
	if ((((i % 21) + 1) % 5) > 1 && str[i - 1] == '#')
		neighbor++;
	return (neighbor);
}
