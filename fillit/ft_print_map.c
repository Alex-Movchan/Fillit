#include "fillit.h"

void	ft_print_map(char **map, int size)
{
	int	i;
	int	j;

	j = 0;
	while (j < size)
	{
		i = 0;
		while (i < size)
		{
			write(1, &map[j][i], 1);
			i++;
		}
		write(1, "\n", 1);
		j++;
	}
}
